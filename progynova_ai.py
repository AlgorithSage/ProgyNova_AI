class CNNLSTMForecaster(nn.Module):
    def __init__(self, input_dim, conv_filters=64, kernel_size=7,
                 lstm_hidden=128, lstm_layers=2, dropout=0.2):
        super().__init__()
        self.conv = nn.Sequential(
            nn.Conv1d(input_dim, conv_filters, kernel_size, padding=kernel_size//2),
            nn.BatchNorm1d(conv_filters), nn.ReLU(), nn.Dropout(dropout),
            nn.Conv1d(conv_filters, conv_filters, 3, padding=1),
            nn.BatchNorm1d(conv_filters), nn.ReLU(), nn.Dropout(dropout))
        self.lstm = nn.LSTM(conv_filters, lstm_hidden, lstm_layers,
                            batch_first=True, dropout=dropout if lstm_layers > 1 else 0)
        self.head = nn.Sequential(nn.Linear(lstm_hidden, 64), nn.ReLU(),
                                  nn.Dropout(dropout), nn.Linear(64, 1))
    def forward(self, x):
        x = self.conv(x.permute(0, 2, 1)).permute(0, 2, 1)
        out, _ = self.lstm(x)
        return self.head(out[:, -1, :]).squeeze(-1)

class PatchTSTTransformer(nn.Module):
    def __init__(self, input_dim, d_model=64, n_heads=4, n_layers=4, dropout=0.2):
        super().__init__()
        self.proj = nn.Linear(input_dim, d_model)
        self.pos = nn.Parameter(torch.randn(1, 200, d_model) * 0.02)
        layer = nn.TransformerEncoderLayer(d_model=d_model, nhead=n_heads,
                dim_feedforward=d_model*4, dropout=dropout, batch_first=True)
        self.encoder = nn.TransformerEncoder(layer, num_layers=n_layers)
        self.head = nn.Sequential(nn.Linear(d_model, 64), nn.ReLU(),
                                  nn.Dropout(dropout), nn.Linear(64, 1))
    def forward(self, x):
        x = self.proj(x) + self.pos[:, :x.size(1), :]
        return self.head(self.encoder(x)[:, -1, :]).squeeze(-1)

# ---- Training executor ----
def run_training(model, name):
    model = model.to(DEVICE)
    criterion = nn.MSELoss()
    optimizer = optim.AdamW(model.parameters(), lr=LR, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, patience=5, factor=0.5)

    n_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"\n{'='*55}")
    print(f"Training: {name} ({n_params:,} params)")
    print(f"{'='*55}")
    print(f"{'Epoch':>6} {'Train':>10} {'Val':>10} {'MAPE':>10}")
    print("-" * 40)

    best_loss, best_weights, patience_ctr = float('inf'), None, 0
    t0 = time.time()

    for epoch in range(1, EPOCHS + 1):
        model.train()
        t_loss = 0
        for bx, by in train_loader:
            bx, by = bx.to(DEVICE), by.to(DEVICE)
            optimizer.zero_grad()
            loss = criterion(model(bx), by)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            t_loss += loss.item() * bx.size(0)

        model.eval()
        v_loss, vp, va = 0, [], []
        with torch.no_grad():
            for bx, by in val_loader:
                bx, by = bx.to(DEVICE), by.to(DEVICE)
                pred = model(bx)
                v_loss += criterion(pred, by).item() * bx.size(0)
                vp.extend(pred.cpu().numpy())
                va.extend(by.cpu().numpy())

        t_loss /= len(train_loader.dataset)
        v_loss /= len(val_loader.dataset)
        scheduler.step(v_loss)

        vp_orig = np.clip(y_scaler.inverse_transform(np.array(vp).reshape(-1,1)).flatten(), 0, None)
        va_orig = y_scaler.inverse_transform(np.array(va).reshape(-1,1)).flatten()
        m = va_orig > 0
        mape = mean_absolute_percentage_error(va_orig[m], vp_orig[m])*100 if m.sum()>0 else float('nan')

        if epoch % 10 == 0 or epoch == 1:
            print(f"{epoch:>6} {t_loss:>10.5f} {v_loss:>10.5f} {mape:>9.2f}%")

        if v_loss < best_loss:
            best_loss = v_loss
            best_weights = {k: v.clone() for k, v in model.state_dict().items()}
            patience_ctr = 0
        else:
            patience_ctr += 1
            if patience_ctr >= PATIENCE:
                print(f"  Early stop at epoch {epoch}")
                break

    print(f"  Done in {time.time()-t0:.1f}s")

    model.load_state_dict(best_weights)
    model.eval()
    preds_sc = []
    with torch.no_grad():
        for bx, _ in test_loader:
            preds_sc.extend(model(bx.to(DEVICE)).cpu().numpy())

    preds = np.clip(y_scaler.inverse_transform(np.array(preds_sc).reshape(-1,1)).flatten(), 0, None)
    m = y_test > 0
    mape = mean_absolute_percentage_error(y_test[m], preds[m])*100
    rmse = np.sqrt(mean_squared_error(y_test, preds))
    print(f"\n  {name}: MAPE={mape:.2f}% | RMSE={rmse:.2f}")
    return preds, mape, rmse

